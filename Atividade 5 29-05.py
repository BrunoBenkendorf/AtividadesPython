#Verificar se número é par ou ímpar
#Comando para definir variavel n do tipo inteiro e em seguida pedir para o usuario
n = int(input("Digite um número:"))
#Comando condição Se o resto de n / 2 for igual a 0
if n % 2 == 0:
#Comando imprimir mensagem
    print("Par")
#Comando condição senão
else:
#Comando imprimir mensagem
    print("Impar")