#Somar dois números inteiros
#Entrada dos dados
#Comando para definir variavel n1 do tipo inteiro e em seguida dar entrada pedindo para o usuário
n1 = int(input("Digite o primeiro número:"))
#Comando para definir variavel n2 do tipo inteiro e em seguida dar entrada pedindo para o usuário
n2 = int(input("Digite o segundo número:"))
#Processamento dos dados
#Comando para efetura a soma entre as variaveis n1 e n2
soma = n1 + n2
#Impressão do resultado
#Comando que efetua a impressão da variavel soma
print("Soma: ", soma)