#Calcular a área de um trialgulo base
#Comando para definir variavel base do tipo real e em seguida pedir para o usuario
base = float(input("Digite a base do triângulo:"))
#Comando para definir variavel altura do tipo real e em seguida pedir para o usuario
altura = float(input("Digite a altura do triângulo:"))

#Comando para area adquirir base x altura em seguida dividos por 2
area = (base*altura)/2
#Comando para imprimir area
print("Área:",area)